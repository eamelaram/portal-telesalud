Route::middleware('auth:sanctum')->get('/user', function (Request $request) {
    return $request->user();
});

Route::post('/token', function (Request $request) {
    $request->validate([
        'email' => 'required|email',
        'password' => 'required',
    ]);

    $user = User::where('email', $request->email)->first();

    if (! $user || ! Hash::check($request->password, $user->password)) {
        throw ValidationException::withMessages([
            'email' => ['The provided credentials are incorrect.'],
        ]);
    }

    return response()->json(['token'=> $user->createToken('SIS-TELESALUD')->plainTextToken]);
});

Route::middleware(['auth:sanctum'])->prefix('sala')->controller(PortalController::class)->group(function () {
    Route::post('/instantanea', 'instantanea');
    Route::post('/programada', 'programada');
});
